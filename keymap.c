static void display_active_layer(led_t led_usb_state) {
    switch (get_highest_layer(layer_state | default_layer_state)) {
        case _COLEMAK_DH:
            oled_clear();
            oled_set_cursor(0, 0);
            if (led_usb_state.caps_lock) {
                oled_write("COLEMAK DH", false);
            } else {
                oled_write("Colemak DH", false);
            }
            break;
        case _QWERTY:
            oled_clear();
            oled_set_cursor(0, 0);
            if (led_usb_state.caps_lock) {
                oled_write("QWERTY", false);
            } else {
                oled_write("Qwerty", false);
            }
            break;
        case _GAMING:
            oled_clear();
            oled_set_cursor(0, 0);
            if (led_usb_state.caps_lock) {
                oled_write("GAMING", false);
            } else {
                oled_write("Gaming", false);
            }
            break;
        case _NAVI:
            oled_clear();
            oled_set_cursor(0, 1);
            if (led_usb_state.caps_lock) {
                oled_write("NAVI & NUMPAD", false);
            } else {
                oled_write("Navi & numpad", false);
            }
            break;
        case _SYMBOL:
            oled_clear();
            oled_set_cursor(0, 2);
            if (led_usb_state.caps_lock) {
                oled_write("SYMBOLS & FKEYS", false);
            } else {
                oled_write("Symbols & fkeys", false);
            }
            break;
        case _OPTIONS:
            oled_clear();
            oled_set_cursor(0, 3);
            if (led_usb_state.caps_lock) {
                oled_write("KEYBOARD OPTIONS", false);
            } else {
                oled_write("Keyboard options", false);
            }
            break;
        default:
            oled_clear();
            oled_set_cursor(0, 1);
            if (led_usb_state.caps_lock) {
                oled_write("Undef caps", false);
            } else {
                oled_write("Undef", false);
            };
    }
    if (led_usb_state.num_lock) {
        oled_write(" [n]", false);
    }
    oled_write("\n", false);
}

static void display_wpm(uint8_t current_wpm) {
    oled_set_cursor(2, 3);
    char wpm_str[4];

    wpm_str[3] = '\0';
    wpm_str[2] = '0' + current_wpm % 10;
    wpm_str[1] = '0' + (current_wpm /= 10) % 10;
    wpm_str[0] = '0' + current_wpm / 10;
    oled_write(wpm_str, false);

    oled_set_cursor(2, 4);
    oled_write_ln("wpm", false);
}

