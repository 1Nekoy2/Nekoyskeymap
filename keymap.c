bool oled_task_user(void) {
    if (!oled_enabled) {
        if (is_oled_on()) {
            oled_off();
            return false;
        }
    }

    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    if (is_keyboard_master()) {
        display_wpm(current_wpm);
        render_luna(0, 13);

    } else {
        display_active_layer(led_usb_state);
    }
    return false;
}

